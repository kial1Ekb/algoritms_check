"""
Создайте программу, которая моделирует работу машины Тьюринга для выполнения сортировки элементов в массиве.
Программа должна принимать массив чисел в качестве ввода, запускать машину Тьюринга для сортировки и выводить
отсортированный массив.
"""




def turing_machine_sort(array):
    """
    Эта функция реализует сортировку вставками. Элементы массива последовательно перебираются, и каждый элемент
    вставляется в уже отсортированную часть массива.
    """
    # Сортировка вставками, имитирующая машину Тьюринга
    for i in range(1, len(array)):
        key = array[i]
        j = i - 1
        # Смещение элементов, которые больше key, на одну позицию вправо
        while j >= 0 and array[j] > key:
            array[j + 1] = array[j]
            j -= 1
        array[j + 1] = key

    return array


def simulate_turing_machine():
    """
    :return:
    """
    import ast
    input_array = ast.literal_eval(input("Введите массив чисел для сортировки (например, [3, 1, 4, 1, 5, 9, 2, 6]): "))

    print("Исходный массив:", input_array)
    sorted_array = turing_machine_sort(input_array)

    print("Отсортированный массив:", sorted_array)

simulate_turing_machine()
