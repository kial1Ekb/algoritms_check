"""
Разработайте программу на выбранном вами языке программирования, которая строит конечный автомат для определения чисел
в двоичной системе, кратных 3. Пользователь вводит число, а программа сообщает, является ли оно кратным 3.
"""

"""
Основная логика: Для двоичного числа при чтении каждого бита справа налево (или наоборот, в зависимости от реализации),
текущий остаток от деления числа на 3 изменяется в зависимости от значения этого бита и предыдущего состояния.
"""

def is_multiple_of_3(binary_number):
    # Состояния автомата:
    # state 0: остаток от деления на 3 равен 0
    # state 1: остаток от деления на 3 равен 1
    # state 2: остаток от деления на 3 равен 2

    # Начальное состояние (остаток 0)
    state = 0

    # Перебор всех символов в строке двоичного числа
    for bit in binary_number:
        if bit == '0':
            # Состояние не изменяется, если текущий бит равен 0
            pass
        elif bit == '1':
            # Переходы между состояниями при бите 1
            if state == 0:
                state = 1
            elif state == 1:
                state = 2
            elif state == 2:
                state = 0

    # Если в конечном состоянии state равен 0, число кратно 3
    return state == 0


# Пользовательский ввод
binary_input = input("Введите двоичное число: ")

# Проверка кратности 3 и вывод результата
if is_multiple_of_3(binary_input):
    print("Число кратно 3.")
else:
    print("Число не кратно 3.")
